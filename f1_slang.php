<div class="container"><h3 align="center">Few things you need to know</div><br>
<div class="container box center">
    <div class="row text-align">
    <h4 class="mt-4">Backmarker</h4>
    <p>The drivers lagging behind are called backmarkers. They are shown blue flag to give way for leading drivers.</p>
    </div>
    <hr>

    <div class="row text-align">
    <h4 class="mt-1">Box</h4>
    <p>It is a reminder to the drivers coming from a controller in the pits that they have a pit stop coming during the lap or in the next lap. 
     The word is derived from the German word ‘Boxenstopp’ which means a pit stop.</p>
    </div>
    <hr>

    <div class="row text-align">
    <h4 class="mt-1">Circuit</h4>
    <p>Formula One circuits are tracks specifically and purposefully built for conducting races. F1 circuits are of two 
     types − Street Circuit and Road Circuit.</p>
    </div>
    <hr>
  
    <div class="row text-align">
    <h4 class="mt-1">Cockpit</h4>
    <p>The seating area of the driver in a F1 car is called the cockpit.</p>
    </div>
    <hr>

    <div class="row text-align">
    <h4 class="mt-1">Constructor</h4>
    <p>Since 1981, FIA has passed a rule that respective F1 teams have to build their own engine and chassis of the car. 
    The owner of the engine and chassis is called the constructor.</p>
    </div>
    <hr>

    <div class="row text-align">
    <h4 class="mt-1">DRS</h4>
    <p>Drag Reduction System (DRS) is allowed to be used in only marked zones on the track. Turbulent air behind the leading car can lead to a drag 
       on the car following it closely, reducing the down force. A flap on the car is then used to reduce the drag and increase the down force which can 
       help the car in overtaking on a straight.</p>
    </div>
    <hr>

    <div class="row text-align">
    <h4 class="mt-1">Formation lap or warm-up lap</h4>
    <p>Cars are driven for a lap from the grid before the start of the race and then 
       get back to grid positions. This is known as the warm-up lap.</p>
    </div>
    <hr>

    <div class="row text-align">
    <h4 class="mt-1">Pits</h4>
    <p>An area of the track separated by a wall. Cars enter team’s pit garage for change of wheels, refuel 
       the car, and set up other changes.</p>
    </div>
    <hr>

    <div class="row text-align">
    <h4 class="mt-1">Pole position</h4>
    <p>Driver who recorded fastest time during qualifying session is awarded the first grid position 
       on race day.</p>
    </div>
    <hr>

    <div class="row text-align">
    <h4 class="mt-1">Polesitter</h4>
    <p>A polesitter is the driver who sets the fastest lap in Q3 of the qualifying sessions. Pole sitters have an advantage if they get 
       away from the pack and hold the lead into the first corner.</p>
    </div>
    <hr>

    <div class="row text-align">
    <h4 class="mt-1">Pit board</h4>
    <p>A board held at pit wall to inform the driver about delta time, remaining number of laps, and current position.
</p>
    </div>
    <hr>

    <div class="row text-align">
    <h4 class="mt-1">Pit wall</h4>
    <p>It is F1 team area where the manager, engineers, and support staff keep close watch on their cars using small screens under a shelter.
</p>
    </div>
    <hr>

    <div class="row text-align">
    <h4 class="mt-1">Steward</h4>
    <p>A high-ranked official appointed for races to make decisions.</p>
    </div>
