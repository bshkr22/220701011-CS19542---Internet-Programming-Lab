      <h2 class="text-center">Practice Session</h2></div><br>
      <img src="flags/prac.jpg" width="100" height="100" alt="" title="Formula 1 Race" class="center">
      <br><br>
      <p>On the surface, these sessions seem to be an hour or so of random running which wouldn't be much interesting 
        for a fan. However, from an engineer’s point of view, practice sessions can be the<b> most useful time throughout the weekend</b>, 
        as they allow teams time to set up the car according to the specifications 
        of each race track thus optimising qualifying and race performance.</p> 

       <p>Furthermore, utilising these practice sessions to test new updates and upgrades to various elements of the car can be a 
        significant contributing factor in their development throughout the season.</p>

     <p>Free practice is split into 3 session: FP1, FP2, FP3.  
        Practice session are for individual teams and their drivers to familiarise themselves with the track and fine-tune their cars.</p>

    <p>During practice sessions, the drivers will try out different types of tyres on the track to find out how long they last. 
       They will also keep a close watch on their fuel consumption with different tyres and tune their engines accordingly.</p>

    <p>Drivers use the practice sessions to get a good feel of the track and the car. They will use this knowledge to get a good position 
       in the qualifying rounds. Watching the practice sessions will let you identify the different cars and drivers during the race. 
       It also gives you a chance to watch all the behind the scenes activities.</p>
