     <div class="row" >
      <h2 class="text-center">Qualifying Session</h2></div><br>
      <img src="flags/q.jpg" width="" height="" alt="" title="Formula 1 Race" class="center">
      <br><br>
      <p>Qualifying sessions are the most exciting experience you will get on a racing weekend. Generally held on Saturday afternoons, there 
    are three qualifying phases Q1, Q2 and Q3. During qualifying sessions, drivers will push their machines to the brink in order to get 
    a good position on the grid.</p>

    <p>In Q1, or the first qualifying round all the 20 cars (if there are 20 cars participating) will take part. The five cars finishing last 
    will be eliminated from any further qualification sessions. They will take the grid positions from 16 to 20 depending on their timings in Q1.</p>

    <p>The remaining 15 cars will participate in the Q2 session. In this session a further five cars that finish last will be eliminated from 
the last qualifying session. They will take grid positions according to their performance in the Q2 session.</p>

<p>Q3 is the last qualifying session and decides the grid position for the remaining 10 cars. In this session, drivers will really work their 
throttles during this session in a bid to win the pole position. The pole position is considered a distinct advantage. The polesitter gets 
an overwhelming advantage in the race.</p>
