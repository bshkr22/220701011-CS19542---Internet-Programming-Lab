      <br><div class="center">
            <p>Each track is sectioned into 3 sectors. Each sector is approximately one-third of the track length. 
                These sectors are used to measure sector times which says how long does it take to drive each sector 
                and the 3 times are added to give the lap time.</p>
        
        <p>A <b>purple</b> sector means that time is the fastest of any driver over that section of the course. 
        When you are watching F1, you may notice a driver with a purple sector that turns green. 
        This will happen when a driver behind them has gone even faster than they did over the same sector. 
        When a driver completes a lap with three purple sectors, they have just set the fastest lap time.</p>

        <p>
        A <b>green</b> sector is a personal best for the driver. Although it is that driver’s personal best, another driver has gone faster.</p>

        <p>
        A <b>yellow</b> sector is slower than the driver’s other times for that sector. Yellow and green sectors only refer to the driver’s past laps, whereas a purple sector is across all drivers.
        </p>

        <p>There are <b>speed trap</b> at the end of each long straights to measure the speed which helps in crowning the fastest lap title to the driver 
        at the end of the race.</p>

        <p>DRS will be enabled for the driver in the straights.
        <b>DRS detection zone</b> tracks the time difference between the driver and the competitor, if it is near to one second DRS will be enabled for them.
        </p>
